    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String roomNumber = jTextField1.getText();
    String roomType = jComboBox1.getSelectedItem().toString();
    String bedType = jComboBox2.getSelectedItem().toString();
    double price = Double.parseDouble(jTextField2.getText());
    
    try {
        Connection con = DriverManager.getConnection(url, username, password);
        // Prepare the SQL statement
        String sql = "INSERT INTO Room (RoomNo, RoomType, BedType, RoomPrice, RoomStatus) VALUES (?, ?, ?, ?, 'Not Booked')";
        PreparedStatement statement = con.prepareStatement(sql);
        statement.setString(1, roomNumber);
        statement.setString(2, roomType);
        statement.setString(3, bedType);
        statement.setDouble(4, price);

        // Execute the query
        int rowsInserted = statement.executeUpdate();

        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(this, "Room added successfully!");
            populateRoomTable();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add the room.");
        }

        // Close the statement
        statement.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
    
    }//GEN-LAST:event_jButton1ActionPerformed

