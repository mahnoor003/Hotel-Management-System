    private void populateRoomTable() {
        DefaultTableModel tableModel = new DefaultTableModel(
        new Object[][]{},
        new String[]{"Room Number", "Room Type", "Bed Type", "Room Price", "Room Status"}
    );
    jTable1.setModel(tableModel);

    try {
        Connection con = DriverManager.getConnection(url, username, password);
        String sql = "SELECT RoomNo, RoomType, BedType, RoomPrice, RoomStatus FROM Room";
        PreparedStatement statement = con.prepareStatement(sql);

        ResultSet resultSet = statement.executeQuery();

        while (resultSet.next()) {
            String roomNo = resultSet.getString("RoomNo");
            String roomType = resultSet.getString("RoomType");
            String bedType = resultSet.getString("BedType");
            double roomPrice = resultSet.getDouble("RoomPrice");
            String roomStatus = resultSet.getString("RoomStatus");

            tableModel.addRow(new Object[]{roomNo, roomType, bedType, roomPrice, roomStatus});
        }

        resultSet.close();
        statement.close();
        con.close();
    } catch (SQLException e) {
        e.printStackTrace();
    }
    }
    

